    def _apply_transformation(self, transform_type):
        selected_indices = self.design_listbox.curselection()
        if not selected_indices: return
        new_designs = {}
        for index in selected_indices:
            design_id = self.design_listbox.get(index)
            original_design = self.design_objects_store[design_id]
            new_design = copy.deepcopy(original_design)
            for layer, matrix in new_design["layout_matrices"].items():
                if transform_type == 'rot90':
                    new_design["layout_matrices"][layer] = np.rot90(matrix)
                    suffix = "_rot90"
                elif transform_type == 'rot180':
                    new_design["layout_matrices"][layer] = np.rot90(matrix, 2)
                    suffix = "_rot180"
                elif transform_type == 'rot270':
                    new_design["layout_matrices"][layer] = np.rot90(matrix, 3)
                    suffix = "_rot270"
                elif transform_type == 'flip_lr':
                    new_design["layout_matrices"][layer] = np.fliplr(matrix)
                    suffix = "_fliplr"
                elif transform_type == 'flip_ud':
                    new_design["layout_matrices"][layer] = np.flipud(matrix)
                    suffix = "_flipud"
            new_id = f"{design_id}{suffix}"; i = 1
            while new_id in self.design_objects_store or new_id in new_designs: new_id = f"{design_id}{suffix}_{i}"; i += 1
            new_design["design_id"] = new_id
            new_designs[new_id] = new_design
        for new_id, design in new_designs.items():
            self.design_objects_store[new_id] = design
            self.design_listbox.insert(tk.END, new_id)

    def _validate_fill_ratio_range(self):
        """验证填充率范围设置"""
        try:
            min_val = float(self.fill_ratio_min_var.get())
            max_val = float(self.fill_ratio_max_var.get())
            
            # 检查范围
            if min_val < 0.1 or min_val > 0.9:
                messagebox.showerror("错误", "最小填充率必须在0.1-0.9之间")
                return False
            if max_val < 0.1 or max_val > 0.9:
                messagebox.showerror("错误", "最大填充率必须在0.1-0.9之间")
                return False
            if min_val > max_val:
                messagebox.showerror("错误", "最小填充率不能大于最大填充率")
                return False
            
            return True
        except ValueError:
            messagebox.showerror("错误", "请输入有效的数字")
            return False

    def _batch_generate(self):
        """批量生成设计，支持随机填充率"""
        try:
            n = int(self.batch_n_var.get())
        except ValueError:
            messagebox.showerror("错误", "请输入一个有效的整数。")
            return
        
        if not self._gather_ui_data():
            return
        
        template_design = self.current_design
        layers = template_design["metadata"]["layers_used"]
        if not layers:
            messagebox.showerror("错误", "请至少添加一个层用于生成。")
            return
        
        # 检查是否使用随机填充率
        if self.random_fill_ratio_var.get():
            # 验证填充率范围
            if not self._validate_fill_ratio_range():
                return
            
            min_ratio = float(self.fill_ratio_min_var.get())
            max_ratio = float(self.fill_ratio_max_var.get())
            
            # 显示确认信息
            confirm_msg = f"将生成 {n} 个设计，每个设计使用随机的填充率（{min_ratio:.2f}-{max_ratio:.2f}）"
            if not messagebox.askyesno("确认批量生成", confirm_msg):
                return
        else:
            # 使用固定填充率
            fixed_ratio = template_design["metadata"]["fill_ratio"]
            confirm_msg = f"将生成 {n} 个设计，每个设计使用固定的填充率（{fixed_ratio:.2f}）"
            if not messagebox.askyesno("确认批量生成", confirm_msg):
                return
        
        # Get base timestamp for this batch
        base_timestamp = datetime.datetime.now().strftime("%Y%m%d_%H%M%S_%f")
        
        for i in range(n):
            new_design = copy.deepcopy(template_design)
            new_design["design_id"] = f"design_{base_timestamp}_{i}"
            
            # 确定填充率
            if self.random_fill_ratio_var.get():
                # 生成随机填充率
                actual_fill_ratio = np.random.uniform(min_ratio, max_ratio)
            else:
                # 使用固定填充率
                actual_fill_ratio = template_design["metadata"]["fill_ratio"]
            
            new_design["metadata"]["fill_ratio"] = template_design["metadata"]["fill_ratio"]
            new_design["metadata"]["actual_fill_ratio"] = actual_fill_ratio
            new_design["layout_matrices"] = generate_random_matrices(layers, fill_ratio=actual_fill_ratio)
            
            self.design_objects_store[new_design["design_id"]] = new_design
            self.design_listbox.insert(tk.END, new_design["design_id"])
        
        # 显示完成信息
        if self.random_fill_ratio_var.get():
            info_msg = f"已成功生成并添加 {n} 组新数据。\n每个设计使用随机填充率（{min_ratio:.2f}-{max_ratio:.2f}）。"
        else:
            info_msg = f"已成功生成并添加 {n} 组新数据。\n所有设计使用固定填充率（{fixed_ratio:.2f}）。"
        
        messagebox.showinfo("完成", info_msg)

