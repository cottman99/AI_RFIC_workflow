    def _update_fill_ratio_label(self, event=None):
        """更新填充率标签显示"""
        ratio = self.fill_ratio_var.get()
        self.fill_ratio_label.config(text=f"{ratio:.2f}")

    def _update_fill_ratio_design(self, event=None):
        """更新当前设计的填充率"""
        if not self._gather_ui_data():
            return
        ratio = self.fill_ratio_var.get()
        self.current_design["metadata"]["fill_ratio"] = ratio
        self.current_design["metadata"]["actual_fill_ratio"] = ratio

    def _update_ui_from_design(self, design_obj):
        self.meta_vars["Design ID"].set(design_obj["design_id"])
        self.meta_vars["Process"].set(design_obj["metadata"].get("process", ""))
        self.meta_vars["Pixel Size (um)"].set(str(design_obj["metadata"].get("pixel_size_um", "")))
        self.meta_vars["Description"].set(design_obj["metadata"].get("description", ""))
        
        # 更新填充率显示
        fill_ratio = design_obj["metadata"].get("fill_ratio", 0.5)
        self.fill_ratio_var.set(fill_ratio)
        self._update_fill_ratio_label()
        
        self.layers_listbox.delete(0, tk.END)
        self.layer_colors = {}
        
        # Clear existing layer visibility checkboxes
        for widget in self.layer_checkboxes_frame.winfo_children():
            widget.destroy()
        self.layer_visibility_vars.clear()
        
        if len(design_obj["metadata"]["layers_used"]) > 0:
            colors = plt.get_cmap('viridis', len(design_obj["metadata"]["layers_used"]))
            for i, layer in enumerate(design_obj["metadata"]["layers_used"]):
                self.layers_listbox.insert(tk.END, layer)
                self.layer_colors[layer] = colors(i)
                
                # Add visibility checkbox
                var = tk.BooleanVar(value=True)
                self.layer_visibility_vars[layer] = var
                cb = ttk.Checkbutton(self.layer_checkboxes_frame, text=layer, variable=var,
                                   command=lambda: self.update_preview(self.current_design))
                cb.pack(side=tk.LEFT, padx=2)
        
        self._update_port_layer_options()

        self.ports_listbox.delete(0, tk.END)
        for port in design_obj["port_definitions"]:
            self.ports_listbox.insert(tk.END, f"ID:{port['port_id']} Name:{port['name']}")
        
        self.update_preview(design_obj)

    def _gather_ui_data(self):
        """从UI控件收集数据，更新到self.current_design。 Returns True on success, False on failure."""
        try:
            self.current_design["metadata"]["process"] = self.meta_vars["Process"].get()
            pixel_size_str = self.meta_vars["Pixel Size (um)"].get()
            self.current_design["metadata"]["pixel_size_um"] = float(pixel_size_str) if pixel_size_str else 0.0
            self.current_design["metadata"]["description"] = self.meta_vars["Description"].get()
            self.current_design["metadata"]["fill_ratio"] = self.fill_ratio_var.get()
            return True
        except ValueError:
            messagebox.showerror("输入错误", "Pixel Size (um) 必须是一个有效的数字。")
            return False

    def _add_layer(self):
        if not self._gather_ui_data(): return
        layer_name = self.layer_var.get().strip()
        if layer_name and layer_name not in self.current_design["metadata"]["layers_used"]:
            self.current_design["metadata"]["layers_used"].append(layer_name)
            self.current_design["layout_matrices"][layer_name] = np.zeros(self.current_design["metadata"]["base_matrix_shape"])
            self._update_ui_from_design(self.current_design)
            self.layer_var.set("")
    
    def _delete_layer(self):
        if not self._gather_ui_data(): return
        selected_indices = self.layers_listbox.curselection()
        if not selected_indices: return
        
        layer_name = self.layers_listbox.get(selected_indices[0])
        if messagebox.askyesno("确认", f"删除层 '{layer_name}' 将同时删除所有关联的端口。确定要继续吗？"):
            self.current_design["metadata"]["layers_used"].remove(layer_name)
            del self.current_design["layout_matrices"][layer_name]
            self.current_design["port_definitions"] = [p for p in self.current_design["port_definitions"] if p['layer'] != layer_name]
            self._update_ui_from_design(self.current_design)

    def _update_port_layer_options(self):
        menu = self.port_layer_menu["menu"]
        menu.delete(0, "end")
        layers = self.current_design["metadata"]["layers_used"]
        if not layers:
            menu.add_command(label="N/A", command=lambda: self.port_vars["Layer"].set("N/A"))
            self.port_vars["Layer"].set("N/A")
        else:
            for layer in layers:
                menu.add_command(label=layer, command=lambda value=layer: self.port_vars["Layer"].set(value))
            if self.port_vars["Layer"].get() not in layers:
                self.port_vars["Layer"].set(layers[0])

    def _add_port(self):
        if not self._gather_ui_data(): return
        try:
            port_def = { "port_id": int(self.port_vars["ID"].get()), "name": self.port_vars["Name"].get(), "layer": self.port_vars["Layer"].get(), "edge": self.port_vars["Edge"].get(), "position_index": int(self.port_vars["Index"].get()) }
            if port_def["layer"] == "N/A": messagebox.showerror("错误", "请先添加一个层。"); return
            if any(p['port_id'] == port_def['port_id'] for p in self.current_design["port_definitions"]): messagebox.showerror("错误", f"端口ID {port_def['port_id']} 已存在。"); return
            self.current_design["port_definitions"].append(port_def)
            self._update_ui_from_design(self.current_design)
            new_id = int(self.port_vars["ID"].get()) + 1
            self.port_vars["ID"].set(str(new_id))
            self.port_vars["Name"].set(f"P{new_id}")
        except ValueError: messagebox.showerror("错误", "端口ID和Index必须是整数。")

