    def update_preview(self, design_obj):
        self.ax.clear()
        N = design_obj["metadata"]["base_matrix_shape"][0]
        grid_size = N + 2
        self.ax.add_patch(Rectangle((0, 0), grid_size, grid_size, facecolor='lightgrey', edgecolor='black', hatch='//', alpha=0.3))
        self.ax.add_patch(Rectangle((1, 1), N, N, facecolor='white', edgecolor='black'))
        legend_patches = []
        
        # Check if we should respect layer visibility
        use_layer_visibility = hasattr(self, 'layer_visibility_vars') and self.layer_visibility_vars
        
        for layer_name in design_obj["metadata"]["layers_used"]:
            # Skip if layer is not visible
            if use_layer_visibility and not self.layer_visibility_vars.get(layer_name, tk.BooleanVar(value=True)).get():
                continue
                
            matrix = design_obj["layout_matrices"].get(layer_name)
            if matrix is None: continue
            color = self.layer_colors.get(layer_name, (0.5,0.5,0.5,1.0))
            for r in range(N):
                for c in range(N):
                    if matrix[r, c] == 1:
                        y_coord = (N - 1 - r) + 1
                        self.ax.add_patch(Rectangle((c + 1, y_coord), 1, 1, facecolor=color, alpha=0.6))
            legend_patches.append(Rectangle((0, 0), 1, 1, facecolor=color, alpha=0.6, label=layer_name))
        
        for port in design_obj["port_definitions"]:
            # Skip port if its layer is not visible
            if use_layer_visibility and not self.layer_visibility_vars.get(port["layer"], tk.BooleanVar(value=True)).get():
                continue
                
            color = self.layer_colors.get(port["layer"], (0.5,0.5,0.5,1.0))
            port_color = darken_color(matplotlib.colors.to_hex(color))
            edge, index = port["edge"], port["position_index"]
            if edge == "left": x, y = 0, (N - 1 - index) + 1
            elif edge == "right": x, y = N + 1, (N - 1 - index) + 1
            elif edge == "bottom": x, y = index + 1, 0
            elif edge == "top": x, y = index + 1, N + 1
            else: continue
            self.ax.add_patch(Rectangle((x, y), 1, 1, facecolor=port_color, edgecolor='black'))
            self.ax.text(x + 0.5, y + 0.5, f"{port['port_id']}", ha='center', va='center', fontsize=8, color='white', weight='bold')
        self.ax.set_xticks(np.arange(0, grid_size + 1, 1)); self.ax.set_yticks(np.arange(0, grid_size + 1, 1))
        self.ax.grid(True, which='both', color='black', linewidth=0.5)
        self.ax.set_xlim(0, grid_size); self.ax.set_ylim(0, grid_size)
        self.ax.set_aspect('equal', adjustable='box')
        self.ax.tick_params(left=False, bottom=False, labelleft=False, labelbottom=False)
        if legend_patches: self.ax.legend(handles=legend_patches, bbox_to_anchor=(1.05, 1), loc='upper left')
        self.fig.tight_layout(rect=[0, 0, 0.85, 1])
        self.canvas.draw()

