class NumpyEncoder(json.JSONEncoder):
    """ Custom encoder for numpy data types """
    def default(self, obj):
        if isinstance(obj, np.ndarray):
            return obj.tolist()
        if isinstance(obj, np.integer):
            return int(obj)
        if isinstance(obj, np.floating):
            return float(obj)
        return json.JSONEncoder.default(self, obj)

def create_new_design_object(suffix=None):
    """创建一个新的、空的design_object字典模板。"""
    timestamp = datetime.datetime.now().strftime("%Y%m%d_%H%M%S_%f")
    design_id = f"design_{timestamp}"
    if suffix is not None:
        design_id = f"{design_id}_{suffix}"
    return {
        "design_id": design_id,
        "metadata": {
            "process": "GF 22FDX+",
            "pixel_size_um": 14.0,
            "description": "Generated by LayoutGeneratorApp",
            "base_matrix_shape": [16, 16],
            "layers_used": [],
            "fill_ratio": 0.5,  # 默认填充率
            "actual_fill_ratio": 0.5  # 实际使用的填充率（批量生成时可能不同）
        },
        "layout_matrices": {},
        "port_definitions": []
    }

def generate_random_matrices(layers, shape=(16, 16), fill_ratio=0.5):
    """为指定的层生成随机的二进制矩阵。"""
    matrices = {}
    for layer in layers:
        matrices[layer] = (np.random.rand(*shape) < fill_ratio).astype(int)
    return matrices

def darken_color(hex_color, factor=0.7):
    """将一个HEX颜色变暗。"""
    if hex_color.startswith('#'):
        hex_color = hex_color[1:]
    rgb = tuple(int(hex_color[i:i+2], 16) for i in (0, 2, 4))
    darker_rgb = tuple(int(c * factor) for c in rgb)
    return f"#{darker_rgb[0]:02x}{darker_rgb[1]:02x}{darker_rgb[2]:02x}"

