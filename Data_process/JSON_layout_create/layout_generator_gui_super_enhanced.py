    def _batch_import_from_disk(self):
        """Batch import JSON files from filesystem (supports Linux and Windows)"""
        file_paths = filedialog.askopenfilenames(
            title="选择JSON文件导入",
            filetypes=[("JSON files", "*.json"), ("All files", "*.*")],
            initialdir=os.getcwd()
        )
        
        if not file_paths:
            return
        
        imported_count = 0
        failed_files = []
        
        for filepath in file_paths:
            try:
                with open(filepath, 'r', encoding='utf-8') as f:
                    design_data = json.load(f)
                
                # Validate basic structure
                if not isinstance(design_data, dict):
                    failed_files.append(f"{os.path.basename(filepath)}: 无效的数据格式")
                    continue
                
                # Ensure required fields exist
                if "design_id" not in design_data:
                    design_data["design_id"] = f"imported_{datetime.datetime.now().strftime('%Y%m%d_%H%M%S_%f')}"
                
                # Handle potential duplicate design IDs
                original_id = design_data["design_id"]
                counter = 1
                while design_data["design_id"] in self.design_objects_store:
                    design_data["design_id"] = f"{original_id}_{counter}"
                    counter += 1
                
                # Convert lists back to numpy arrays
                if "layout_matrices" in design_data:
                    for layer, matrix in design_data["layout_matrices"].items():
                        if isinstance(matrix, list):
                            design_data["layout_matrices"][layer] = np.array(matrix)
                
                # 确保有必要的字段，兼容旧版本
                if "metadata" in design_data:
                    if "fill_ratio" not in design_data["metadata"]:
                        design_data["metadata"]["fill_ratio"] = 0.5
                    if "actual_fill_ratio" not in design_data["metadata"]:
                        design_data["metadata"]["actual_fill_ratio"] = design_data["metadata"]["fill_ratio"]
                
                self.design_objects_store[design_data["design_id"]] = design_data
                self.design_listbox.insert(tk.END, design_data["design_id"])
                imported_count += 1
                
            except json.JSONDecodeError:
                failed_files.append(f"{os.path.basename(filepath)}: JSON格式错误")
            except Exception as e:
                failed_files.append(f"{os.path.basename(filepath)}: {str(e)}")
        
        if failed_files:
            messagebox.showwarning("导入警告", f"以下文件导入失败:\n" + "\n".join(failed_files))
        
        messagebox.showinfo("导入完成", f"成功导入 {imported_count} 个设计\n失败: {len(failed_files)} 个")

