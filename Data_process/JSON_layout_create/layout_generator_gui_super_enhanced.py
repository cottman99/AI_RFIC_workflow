    def _move_item_in_list(self, list_type, direction):
        if not self._gather_ui_data(): return
        if list_type == 'layer':
            listbox = self.layers_listbox
            data_list = self.current_design["metadata"]["layers_used"]
        else:
            listbox = self.ports_listbox
            data_list = self.current_design["port_definitions"]
        selected_indices = listbox.curselection()
        if not selected_indices: return
        idx = selected_indices[0]
        if direction == -1 and idx > 0:
            data_list[idx], data_list[idx-1] = data_list[idx-1], data_list[idx]
            new_selection_idx = idx - 1
        elif direction == 1 and idx < len(data_list) - 1:
            data_list[idx], data_list[idx+1] = data_list[idx+1], data_list[idx]
            new_selection_idx = idx + 1
        else:
            return
        if list_type == 'layer':
            self.current_design["layout_matrices"] = {layer: self.current_design["layout_matrices"][layer] for layer in data_list}
        self._update_ui_from_design(self.current_design)
        listbox.selection_set(new_selection_idx)

